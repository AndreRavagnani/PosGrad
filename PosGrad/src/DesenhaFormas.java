
public abstract class DesenhaFormas {
	
	abstract void desenhar();
	
	private int dadoInteiro;
	public int getDadoInteiro() {
		return dadoInteiro;
	}
	public void setDadoInteiro(int dadoInteiro) {
		this.dadoInteiro = dadoInteiro;
	}
	public float getDadoFlutuante() {
		return dadoFlutuante;
	}
	public void setDadoFlutuante(float dadoFlutuante) {
		this.dadoFlutuante = dadoFlutuante;
	}
	private float dadoFlutuante;
	
}
